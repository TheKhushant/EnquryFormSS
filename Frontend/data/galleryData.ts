// ====================== HELPER FUNCTIONS ======================

export interface CategoryDescription {
    [key: string]: string;
}

export const extractCategory = (relativePath: string): string => {
    return relativePath.split("/")[0];
};

export const formatTitle = (folderName: string): string => {
    if (!folderName) return "General Gallery";

    // Clean and format long college names nicely
    return folderName
        .replace(/\s*\(.*?\)/g, "")           // Remove acronyms in brackets like (ACET)
        .replace(/_/g, " ")
        .trim()
        .split(/\s+/)
        .map((word) => word.charAt(0).toUpperCase() + word.slice(1).toLowerCase())
        .join(" ");
};

export const getDescription = (category: string): string => {
    return (
        CATEGORY_DESCRIPTIONS[category] ||
        `Collection of memorable moments from ${formatTitle(category)}`
    );
};

export const generateGalleryItems = (basePath: string = "/GALLARY/"): GalleryItem[] => {
    return RAW_IMAGE_PATHS.map((relativePath, index) => {
        const category = extractCategory(relativePath);
        const fileName = relativePath.split("/").pop() || "";

        const encodedUrl =
            basePath +
            relativePath
                .split("/")
                .map((seg) => encodeURIComponent(seg))
                .join("/");

        return {
            id: index + 1,
            type: "image",
            category,
            title: formatTitle(category),
            description: getDescription(category),
            image: encodedUrl,
            badge: formatTitle(category).split(" ").slice(0, 2).join(" "),
            fileName,
            rawPath: relativePath,
        };
    });
};

export const getUniqueCategories = (): string[] => {
    const categories = new Set<string>();
    RAW_IMAGE_PATHS.forEach((path) => {
        categories.add(extractCategory(path));
    });
    return Array.from(categories);
};